// 生成 Flutter Windows 交叉构建所需的 `flutter/ephemeral/` 产物。
//
// 正常情况下 `flutter build windows` 会在 `windows/flutter/ephemeral/` 下生成一
// 批文件，供 `flutter/CMakeLists.txt` 使用：
//   - generated_config.cmake：FLUTTER_ROOT / PROJECT_DIR / FLUTTER_VERSION* 等
//   - flutter_windows.dll(.lib) 嵌入器与导入库
//   - 5 个嵌入器头文件 + cpp_client_wrapper/ 源码
//   - icudtl.dat
// 并通过一个跑 `tool_backend.bat` 的 `flutter_assemble` 自定义目标在原生构建阶段
// 回调 flutter 工具。
//
// 在 Linux 上交叉编译时，这些都不会自动发生：本模块负责把嵌入器文件铺到
// ephemeral/、写出 generated_config.cmake，并把 `flutter_assemble` 中和成空目标
// （tool_backend.bat 无法在 Linux 运行，且 kernel/AOT 已由本工具自行完成）。
//
// 本文件只放**纯函数**（便于单测）；实际的文件拷贝与落盘由 pipeline 编排。

/// 解析后的应用版本，对应 CMake 里的 FLUTTER_VERSION* 变量。
class FlutterVersionParts {
  const FlutterVersionParts({
    required this.full,
    required this.major,
    required this.minor,
    required this.patch,
    required this.build,
  });

  /// 原始版本串，例如 `1.0.0+1`。
  final String full;
  final int major;
  final int minor;
  final int patch;

  /// `+` 之后的构建号；无法解析为单一整数时为 0（与 flutter_tools 行为一致）。
  final int build;
}

/// 解析 pubspec 的 `version` 字段（如 `1.2.3+4`）。
///
/// 缺省或非法时回退到 `1.0.0`（major/minor/patch/build 均按 flutter_tools 的
/// 默认取 1.0.0 / build 0）。
FlutterVersionParts parseFlutterVersion(String? raw) {
  final trimmed = raw?.trim();
  if (trimmed == null || trimmed.isEmpty) {
    return const FlutterVersionParts(
        full: '1.0.0', major: 1, minor: 0, patch: 0, build: 0);
  }

  final plusIndex = trimmed.indexOf('+');
  final namePart = plusIndex >= 0 ? trimmed.substring(0, plusIndex) : trimmed;
  final buildPart = plusIndex >= 0 ? trimmed.substring(plusIndex + 1) : '';

  final nums = namePart.split('.');
  int at(int i) => i < nums.length ? (int.tryParse(nums[i].trim()) ?? 0) : 0;

  // 仅当构建号是单一整数时才采用，否则按 flutter_tools 规则回退到 0。
  final build = int.tryParse(buildPart.trim()) ?? 0;

  return FlutterVersionParts(
    full: trimmed,
    major: at(0),
    minor: at(1),
    patch: at(2),
    build: build,
  );
}

/// 渲染 `generated_config.cmake` 的内容。格式对齐 flutter_tools 的
/// `writeGeneratedCmakeConfig`：用 `file(TO_CMAKE_PATH ...)` 规范化路径，并以
/// `PARENT_SCOPE` 导出版本变量给 runner/ 子目录使用。
String renderGeneratedConfigCmake({
  required String flutterRoot,
  required String projectDir,
  required FlutterVersionParts version,
}) {
  // Linux 路径无反斜杠，无需像 Windows 那样转义；TO_CMAKE_PATH 亦可安全处理。
  return '''
# Generated by flutter_build. Do not commit.
file(TO_CMAKE_PATH "$flutterRoot" FLUTTER_ROOT)
file(TO_CMAKE_PATH "$projectDir" PROJECT_DIR)

set(FLUTTER_VERSION "${version.full}" PARENT_SCOPE)
set(FLUTTER_VERSION_MAJOR ${version.major} PARENT_SCOPE)
set(FLUTTER_VERSION_MINOR ${version.minor} PARENT_SCOPE)
set(FLUTTER_VERSION_PATCH ${version.patch} PARENT_SCOPE)
set(FLUTTER_VERSION_BUILD ${version.build} PARENT_SCOPE)

# 交叉构建下 tool_backend 不会被调用（flutter_assemble 已被中和），此列表仅为
# 保持与 flutter 生成物结构一致。
list(APPEND FLUTTER_TOOL_ENVIRONMENT
  "FLUTTER_ROOT=$flutterRoot"
  "PROJECT_DIR=$projectDir"
)
''';
}

/// 标记 flutter 模板中「Flutter tool backend」区段起点的注释。该区段（含
/// phony 输出、`add_custom_command` 与 `add_custom_target(flutter_assemble ...)`）
/// 在标准模板里是文件的最后一段。
const String kFlutterToolBackendMarker = '# === Flutter tool backend ===';

/// 把 `flutter/CMakeLists.txt` 里的 `flutter_assemble` 目标中和为**空目标**。
///
/// 原目标会通过一个 phony 输出每次都调用 `tool_backend.bat`（Windows 批处理），
/// 在 Linux 交叉构建中必然失败；而它产出的嵌入器/包装源码已由本工具预先铺到
/// ephemeral/、kernel 与 AOT 也已单独完成。因此只需保留一个空的 flutter_assemble
/// 目标以满足其它目标（flutter / 各 wrapper / runner）的 `add_dependencies`。
///
/// 找到标记 [kFlutterToolBackendMarker] 后，截断其后内容并追加空目标；找不到标记
/// 时原样返回（交由调用方决定是否告警）。
String neutralizeFlutterAssemble(String cmakeContent) {
  final idx = cmakeContent.indexOf(kFlutterToolBackendMarker);
  if (idx < 0) return cmakeContent;

  final head = cmakeContent.substring(0, idx);
  return '$head'
      '# === Flutter tool backend (由 flutter_build 中和) ===\n'
      '# 原区段会调用 Windows 批处理后端，无法在 Linux 交叉构建中运行；\n'
      '# kernel/AOT 与嵌入器文件已由 flutter_build 预先生成到 ephemeral/。\n'
      '# 保留空目标以满足其它目标的依赖。\n'
      'add_custom_target(flutter_assemble)\n';
}

