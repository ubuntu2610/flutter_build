// Wine 包装脚本生成器。
//
// 在 Linux 上交叉构建 Windows 目标时，部分 Windows 工具（例如
// `gen_snapshot.exe`）需要在 Wine 下运行。与其在每处都手动拼 Wine 环境
// 变量，这里集中生成一个可执行的 bash 包装脚本，并暴露统一的运行环境，供
// 流水线各阶段复用。

import 'dart:io';

import 'package:path/path.dart' as p;

import '../toolchain.dart';

/// 为一个给定的 [buildRoot] 生成并维护 Wine 包装脚本。
class WineWrapper {
  WineWrapper({
    required this.toolchain,
    required this.buildRoot,
  });

  final Toolchain toolchain;
  final String buildRoot;

  /// 生成的 bash 脚本绝对路径。
  String get scriptPath => p.join(buildRoot, 'wine_wrapper.sh');

  /// Wine 的 prefix 目录（位于构建根下，避免污染用户 HOME）。
  String get winePrefix => p.join(buildRoot, '.wineprefix');

  /// 把包装脚本落盘并标记为可执行。幂等：重复调用只覆盖重写。
  Future<void> materialize() async {
    final dir = Directory(buildRoot);
    if (!dir.existsSync()) {
      dir.createSync(recursive: true);
    }

    final content = '#!/usr/bin/env bash\n'
        '# Generated by flutter_build. Do not edit.\n'
        'export WINEPREFIX="$winePrefix"\n'
        'export WINEDEBUG=-all\n'
        'exec "${toolchain.wineExecutable}" "\$@"\n';

    final file = File(scriptPath);
    await file.writeAsString(content);
    // 确保脚本对 owner 可执行（测试会检查 owner exec 位）。
    Process.runSync('chmod', <String>['+x', scriptPath]);
  }

  /// 运行 Wine 包装脚本时应注入的环境变量。
  Map<String, String> environment() => <String, String>{
        'WINEPREFIX': winePrefix,
        'WINEDEBUG': '-all',
      };
}
